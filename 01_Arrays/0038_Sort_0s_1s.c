/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    Sort0sAnd1s();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void Sort0sAnd1s(void)
{
    int Arr[8] = {0,1,2,0,1,2,1,0};
    int temp = 0;

    printf("Array before sorting: ");
    for(int i = 0; i < 8; ++i)
    {
        printf("%d ",Arr[i]);
    }
    printf("\n");

    for(int i =0; i < 8; ++i)
    {
        for(int j = 0; j < 8; ++j)
        {
            if(Arr[i] < Arr[j])
            {
                temp = Arr[i];
                Arr[i] = Arr[j];
                Arr[j] = temp;
            }
        }
    }

    printf("Array After sorting: ");
    for(int i = 0; i < 8; ++i)
    {
        printf("%d ",Arr[i]);
    }
    printf("\n");
}