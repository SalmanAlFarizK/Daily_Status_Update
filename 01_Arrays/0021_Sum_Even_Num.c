/*****************************************************************************
 * Gobal Includes
 ****************************************************************************/
#include <stdio.h>
#include <stdint.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void SumEvenNumbers(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    SumEvenNumbers();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void SumEvenNumbers(void)
{
    int Arr[5] = {2,3,4,5,6};
    int EvenSum = 0;

    for(int i = 0; i < 5; ++i)
    {
        if(Arr[i] % 2 == 0)
        {
            EvenSum += Arr[i];
        }
    }

    printf("Sum of even numbers in the array: %d\n", EvenSum);
    return;
}