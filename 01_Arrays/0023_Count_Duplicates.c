/*****************************************************************************
 * Gobal Includes
 ****************************************************************************/
#include <stdio.h>
#include <stdint.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void CountDuplicates(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    CountDuplicates();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void CountDuplicates(void)
{
    int Arr[6] = {1,2,3,1,4,2};
    int duplicateCount = 0;

    for(int i = 0; i < 6; ++i)
    {
        for(int j = i + 1; j < 6; ++j)
        {
            if(Arr[i] == Arr[j])
            {
                duplicateCount += 1;
                break;
            }
        }
    }
    printf("Number of duplicate elements in the array: %d\n", duplicateCount);
    return;
}