/*****************************************************************************
 * Gobal Includes
 ****************************************************************************/
#include <stdio.h>
#include <stdint.h>
#include <stdlib.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void CountPairWithSum(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    CountPairWithSum();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void CountPairWithSum(void)
{
    int Arr[5] = {1,5,7,-1,5};
    int Sum = 6;
    int Count = 0;

    for(int i = 0; i < 5; ++i)
    {
        for(int j = i + 1; j < 5; ++j)
        {
            if(Arr[i] + Arr[j] == Sum)
            {
                Count++;
            }
        }
    }
    printf("Number of pairs with sum %d: %d\n", Sum, Count);
    return;
}