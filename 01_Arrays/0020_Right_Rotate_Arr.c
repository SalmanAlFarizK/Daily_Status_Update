/*****************************************************************************
 * Gobal Includes
 ****************************************************************************/
#include <stdio.h>
#include <stdint.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void RightRotArrByN(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    RightRotArrByN();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void RightRotArrByN(void)
{
    int Arr[5] = {1, 2, 3, 4, 5};
    int N = 2;

    for(int i = 0; i < N; ++i)
    {
        int Last = Arr[4];
        for(int j = 4; j > 0; --j)
        {
            Arr[j] = Arr[j - 1];
        }
        Arr[0] = Last;
    }

    printf("Array after right rotation by %d positions: ", N);
    for(int i = 0; i < 5; ++i)
    {
        printf("%d ", Arr[i]);
    }
    printf("\n");
    return;
}