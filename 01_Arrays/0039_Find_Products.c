#include <stdio.h>
#include <stdint.h>
#include <stdlib.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void FindProductsOfElts(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    FindProductsOfElts();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void FindProductsOfElts(void)
{
    int Arr[4] = {1,2,3,4};
    int iProduct = 1;

    for(int i = 0; i < 4; ++i)
    {
        iProduct *= Arr[i];
    }
    printf("The product of all the element in the array is : %d\n",iProduct);

    return;
}