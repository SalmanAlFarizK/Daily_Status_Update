/*****************************************************************************
* Function Definitions
*****************************************************************************/
void FindLeaders(void)
{
    int Arr[6] = {16,17,4,3,5,2};

    for(int i = 0; i < 6; ++i)
    {
        for(int j = i + 1; j < 6; ++j)
        {
            if(Arr[i] <= Arr[j])
            {
                break; 
            }
            if(j == 5) 
            {
                printf("%d ", Arr[i]);
            }
        }
    }
    printf("%d ", Arr[5]);
    printf("\n");
    return;
}