/*****************************************************************************
 * Gobal Includes
 ****************************************************************************/
#include <stdio.h>
#include <stdint.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void LeftRotArrByN(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    LeftRotArrByN();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void LeftRotArrByN(void)
{
    int Arr[5] = {1,2,3,4,5};
    int N = 2;

    for(int i = 0; i < N; ++i)
    {
        int First = Arr[0];
        for(int j = 0; j < 4; ++j)
        {
            Arr[j] = Arr[j + 1];
        }
        Arr[4] = First;
    }

    printf("Array after left rotation by %d positions: ", N);
    for(int i = 0; i < 5; ++i)
    {
        printf("%d ", Arr[i]);
    }
    printf("\n");
    return;
}