/*****************************************************************************
 * Gobal Includes
 ****************************************************************************/
#include <stdio.h>
#include <stdint.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void DiffBwMaxnMin(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    DiffBwMaxnMin();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void DiffBwMaxnMin(void)
{
    int Arr[5] = {5,1,9,3,6};
    int Max = Arr[0], Min = Arr[0];

    for(int i = 0; i < 5; ++i)
    {
        if(Arr[i] > Max)
        {
            Max = Arr[i];
        }
        if(Arr[i] < Min)
        {
            Min = Arr[i];
        }
    }

    printf("Difference between maximum and minimum element is: %d\n", Max - Min);

    return;
}