#include <stdio.h>
#include <stdint.h>
#include <stdlib.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void MissingNumbInArray(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    MissingNumbInArray();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void MissingNumbInArray(void)
{
    int Arr[6] = {1,2,4,5,6};

    for(int i = 1; i <= 6; ++i)
    {
        int found = 0;
        for(int j = 0; j < 6; ++j)
        {
            if(Arr[j] == i)
            {
                found = 1;
                break;
            }
        }
        if(!found)
        {
            printf("Missing number in the array is: %d\n", i);
        }
    }
    return;
}