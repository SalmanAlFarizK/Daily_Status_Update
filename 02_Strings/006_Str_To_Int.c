#include <stdio.h>
#include <stdint.h>
#include <stdlib.h>
#include <string.h>

/*****************************************************************************
 * Function Declarations
 ****************************************************************************/
void NumericStrToInt(void);

/*****************************************************************************
 * Main Function
 ****************************************************************************/
int main()
{
    NumericStrToInt();
    return 0;
}

/*****************************************************************************
 * Function Definitions
 ****************************************************************************/
void NumericStrToInt(void)
{
    char str[] = "1234";
    int num = 0;
    int i = 0;
    while (str[i] != '\0')
    {
        if (str[i] >= '0' && str[i] <= '9')
        {
            num = num * 10 + (str[i] - '0');
        }
        i++;
    }
    printf("The integer value is: %d\n", num);
    return;
}